export function buildFinalPrompt(
  userInput: string,
  style: StyleTemplate,
  mode: 'main' | 'detail' = 'main',
  aspectRatio?: string
): string {
  const stylePrompt = mode === 'main' ? style.mainImagePrompt : style.detailImagePrompt;
  const modeTag = mode === 'main'
    ? '【模式：电商主图】信息密集型，图文并茂，产品图+多段卖点文案+促销标签+数据对比'
    : '【模式：电商详情图】竖版长图，说明书式排版，分模块展示，每个模块图文搭配、箭头标注、数据可视化，信息丰富专业';

  const aspectText = aspectRatio
    ? mode === 'main'
      ? `图片比例${aspectRatio}，产品居中占60%，卖点文字占40%`
      : `图片比例${aspectRatio}，竖版长图，从上到下分模块，阅读路径清晰`
    : '';

  return [
    `用户需求：${userInput}`,
    `选用风格：${style.name} (${style.description})`,
    modeTag,
    mode === 'main' ? `主图基础规则：${MAIN_IMAGE_BASE}` : `详情图基础规则：${DETAIL_IMAGE_BASE}`,
    stylePrompt,
    GLOBAL_QUALITY,
    aspectText,
    `负面排除：${style.negativePrompt}`,
  ]
    .filter(Boolean)
    .join('\n\n');
}

